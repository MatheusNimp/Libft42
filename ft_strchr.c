char	*ft_strchr(const char *str, int c)
{
	int i;

	i = -1;
	while (str[++i] != '\0')
		if (str[i] == c)
			return((char *)&str[i]);
    if (c == '\0')
        return ((char *)&str[i]);
    return ((void *)0);
}
